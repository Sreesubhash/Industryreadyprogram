print("He adds a 1 ltr of water for every 4 ltrs") # a = 4 + 1 = 5ltrs
a = 4 + 1

# Each liter gets the total rate "S"

S = 3.25 * a
print(f"Mixture of milk rate at {S}rs")

print("Vendor sells the milk at rate of 4.15/ltr")

P = 4.15 * a  # increase the price of mixture of 5 ltrs milk
print(f"Vendor sells the 5lts milk at {P}rs")

D = P - S
print(f"vendor earn the gain on every 5lts is {D}rs")

G = D / 5
print(f"vendor gain on every ltr {G}")